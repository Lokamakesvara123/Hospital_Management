--To create the warehouse

CREATE WAREHOUSE IF NOT EXISTS HOSPITAL_MANAGE_WH
  WAREHOUSE_SIZE = 'XSMALL'
  AUTO_SUSPEND = 60
  AUTO_RESUME = TRUE;

--create the database

CREATE DATABASE IF NOT EXISTS HOSPITAL_MANAGE_DB;
USE DATABASE HOSPITAL_MANAGE_DB;

--create the Schema 

CREATE SCHEMA HOSPITAL_SCHEMA;
USE SCHEMA HOSPITAL_SCHEMA;

CREATE FILE FORMAT CSV_FORMAT
TYPE = CSV
SKIP_HEADER = 1
FIELD_OPTIONALLY_ENCLOSED_BY='"';

--create the stage 

CREATE STAGE HOSPITAL_STAGE
FILE_FORMAT = CSV_FORMAT;

--create the DIM_PATIENT 

CREATE OR REPLACE TABLE DIM_PATIENT (
    PATIENT_ID NUMBER NOT NULL,
    FULL_NAME STRING,
    DOB DATE,
    GENDER STRING,
    PHONE STRING,
    EMAIL STRING,
    CITY STRING,
    STATE STRING,
    REGISTRATION_DATE DATE
);

--create the DIM_DOCTORS

CREATE OR REPLACE TABLE DIM_DOCTORS AS
SELECT DISTINCT
  DOCTOR_ID,
  DOCTOR_NAME,
  DEPARTMENT
FROM FACT_APPOINTMENTS;

--create the appointment 

CREATE OR REPLACE TABLE FACT_APPOINTMENT (
    APPT_ID NUMBER,
    APPT_DATE DATE NOT NULL,
    PATIENT_ID NUMBER NOT NULL,
    DOCTOR_ID NUMBER,
    DEPARTMENT STRING,
    SLOT STRING,
    STATUS STRING CHECK (
        STATUS IN ('Scheduled','Completed','No-Show','Cancelled')
    )
);

--create the billing 

CREATE OR REPLACE TABLE FACT_BILLING (
    BILL_ID NUMBER,
    BILL_DATE DATE NOT NULL,
    PATIENT_ID NUMBER NOT NULL,
    SERVICE_CODE STRING,
    SERVICE_DESC STRING,
    DEPARTMENT STRING,
    GROSS_AMOUNT NUMBER(10,2),
    DISCOUNT_AMOUNT NUMBER(10,2),
    TAX_AMOUNT NUMBER(10,2),
    NET_AMOUNT NUMBER(10,2) CHECK (NET_AMOUNT >= 0),
    PAYMENT_MODE STRING,
    INSURER_NAME STRING
);

--exception record

CREATE OR REPLACE TABLE EXCEPTION_RECORDS (
    ERROR_TYPE STRING,
    RECORD_DETAILS STRING,
    LOAD_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP()
);

