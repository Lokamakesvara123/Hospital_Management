--create view 

CREATE or replace VIEW VW_DEPT_APPT_SUMMARY AS
SELECT
DEPARTMENT,
COUNT(*) TOTAL_APPOINTMENTS
FROM FACT_APPOINTMENTS
GROUP BY DEPARTMENT;

select * from VW_DEPT_APPT_SUMMARY;

-- create a view for revenue 

CREATE VIEW VW_REVENUE_BY_DEPT AS
SELECT
DEPARTMENT,
SUM(NET_AMOUNT) TOTAL_REVENUE
FROM FACT_BILLING
GROUP BY DEPARTMENT;

select * from VW_REVENUE_BY_DEPT;

-- create a view for daily appointment 

CREATE OR REPLACE VIEW VW_DAILY_OPD_TREND AS
SELECT
APPT_DATE,
COUNT(*) TOTAL_VISITS
FROM FACT_APPOINTMENTS
GROUP BY APPT_DATE;

select * from VW_DAILY_OPD_TREND;

