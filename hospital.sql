--secure view 
USE SCHEMA HOSPITAL_SCHEMA;
CREATE ROLE IF NOT EXISTS DOCTOR_ROLE;
CREATE ROLE IF NOT EXISTS BILLING_ROLE;
CREATE ROLE IF NOT EXISTS ANALYST_ROLE;
CREATE ROLE IF NOT EXISTS AUDITOR_ROLE;
CREATE ROLE IF NOT EXISTS ADMIN_ROLE;

--doctor view
CREATE OR REPLACE SECURE VIEW SV_DOCTOR_VIEW AS
SELECT
    P.PATIENT_ID,P.FULL_NAME,P.GENDER,A.APPT_ID,A.APPT_DATE,A.DEPARTMENT,A.STATUS
FROM DIM_PATIENT P
JOIN FACT_APPOINTMENTS A
ON P.PATIENT_ID = A.PATIENT_ID;

GRANT SELECT ON VIEW SV_DOCTOR_VIEW
TO ROLE DOCTOR_ROLE;

--billing view
CREATE OR REPLACE SECURE VIEW SV_BILLING_VIEW AS
SELECT
    BILL_ID,BILL_DATE,PATIENT_ID,DEPARTMENT,NET_AMOUNT,PAYMENT_MODE,INSURER_NAME
FROM FACT_BILLING;

GRANT SELECT ON VIEW SV_BILLING_VIEW
TO ROLE BILLING_ROLE;

--analystic view
CREATE OR REPLACE SECURE VIEW SV_ANALYST_VIEW AS
SELECT
    DEPARTMENT,COUNT(*) TOTAL_APPOINTMENTS
FROM FACT_APPOINTMENT
GROUP BY DEPARTMENT;

GRANT SELECT ON VIEW SV_ANALYST_VIEW
TO ROLE ANALYST_ROLE;

CREATE TABLE IF NOT EXISTS APPOINTMENT_AUDIT
(
 APPT_ID NUMBER, ACTION_TYPE STRING, ACTION_TIME TIMESTAMP
);

--auditor role
CREATE OR REPLACE SECURE VIEW SV_AUDITOR_VIEW AS
SELECT
    APPT_ID, ACTION_TYPE, ACTION_TIME
FROM APPOINTMENT_AUDIT;

GRANT SELECT ON VIEW SV_AUDITOR_VIEW
TO ROLE AUDITOR_ROLE;

--admin role
CREATE ROLE IF NOT EXISTS ADMIN_ROLE;
GRANT ALL PRIVILEGES
ON DATABASE HOSPITAL_MANAGE_DB
TO ROLE ADMIN_ROLE;

ALTER COMPUTE POOL SYSTEM_COMPUTE_POOL_CPU SUSPEND;
ALTER COMPUTE POOL SYSTEM_COMPUTE_POOL_CPU RESUME;
